    <div class="text-center">
        <h2 class="text-2xl font-bold text-white mb-2">PETUNJUK PENGISIAN</h2>
        <p class="text-base text-white mb-2">
            Model Asesmen Kinerja Sekolah Pendidikan Menengah<br>
            <span class="italic text-gray-300">(Integrasi MBNQA for Education dan BAN-S/M 2024)</span>
        </p>
    </div>

    <div class="space-y-6 text-white">
        <div>
            <h3 class="font-semibold text-lg text-blue-300 mb-2">1. Tujuan Asesmen</h3>
            <p class="mb-2">
                Instrumen ini digunakan untuk menilai kinerja sekolah secara komprehensif berdasarkan integrasi model
                Malcolm Baldrige National Quality Award (MBNQA) for Education dan Instrumen Akreditasi BAN-S/M 2024.
                Hasil asesmen dapat digunakan sebagai:
            </p>
            <ul class="list-disc ml-6">
                <li>Bahan evaluasi diri sekolah (EDS).</li>
                <li>Acuan peningkatan mutu berkelanjutan.</li>
                <li>Dasar pengambilan keputusan manajerial.</li>
            </ul>
        </div>

        <div>
            <h3 class="font-semibold text-lg text-blue-300 mb-2">2. Komponen Asesmen</h3>
            <p class="mb-2">
                Asesmen terdiri atas dua dimensi utama:
            </p>
            <ul class="list-disc ml-6">
                <li><strong>Proses (ADLI)</strong> &rarr; menilai pendekatan, penerapan, pembelajaran, dan integrasi
                    praktik sekolah.</li>
                <li><strong>Hasil (LeTCI)</strong> &rarr; menilai tingkat capaian, tren perkembangan, perbandingan, dan
                    integrasi hasil sekolah.</li>
            </ul>
        </div>

        <div>
            <h3 class="font-semibold text-lg text-blue-300 mb-2">3. Skala Penilaian</h3>
            <p class="mb-2">Setiap indikator dinilai dengan skala berikut:</p>
            <ul class="list-disc ml-6">
                <li><strong>4 = Sangat Baik</strong> &rarr; praktik/hasil unggul, konsisten, inovatif, dan terintegrasi.
                </li>
                <li><strong>3 = Baik</strong> &rarr; praktik/hasil baik dan terdokumentasi, namun belum sepenuhnya
                    inovatif/terintegrasi.</li>
                <li><strong>2 = Cukup</strong> &rarr; praktik/hasil ada, tetapi parsial, belum konsisten, masih
                    administratif.</li>
                <li><strong>1 = Kurang</strong> &rarr; praktik/hasil belum berjalan atau sekadar formalitas.</li>
            </ul>
        </div>

        <div>
            <h3 class="font-semibold text-lg text-blue-300 mb-2">4. Langkah Pengisian</h3>
            <ol class="list-decimal ml-6 space-y-1">
                <li>Bacalah setiap indikator dengan cermat.</li>
                <li>Diskusikan dengan tim penjaminan mutu/asesor internal untuk menentukan deskripsi kondisi nyata di
                    sekolah.</li>
                <li>Cocokkan kondisi sekolah dengan rubrik penilaian yang tersedia.</li>
                <li>Beri skor (1–4) sesuai dengan kondisi yang paling mendekati.</li>
                <li>Cantumkan bukti fisik/nonfisik yang relevan (dokumen, foto, data, laporan, hasil survei, dll.).</li>
            </ol>
        </div>

        <div>
            <h3 class="font-semibold text-lg text-blue-300 mb-2">5. Pengolahan Skor</h3>
            <ul class="list-disc ml-6">
                <li>Jumlahkan skor pada setiap dimensi.</li>
                <li>Nilai total diperoleh dari skor x bobot dengan ketentuan sebagai berikut:</li>
            </ul>
            <div class="ml-6">
                <ul class="list-disc">
                    <li>Komponen Kepemimpinan Kepala Sekolah = 15 %</li>
                    <li>Komponen Pelanggan (Siswa, Orang Tua, dan Masyarakat) = 15 %</li>
                    <li>Komponen Pengukuran, Analisis, Managemen Pengetahuan = 15 %</li>
                    <li>Komponen Tenaga Kerja (Tenaga Pendidik dan Kependidikan) = 15 %</li>
                    <li>Komponen Proses (Operasional) = 15 %</li>
                    <li>Komponen Hasil = 20 %</li>
                </ul>
            </div>
            <p class="mt-2">Interpretasi hasil:</p>
            <ul class="list-disc ml-6">
                <li>3.51 – 4.00 = Unggul</li>
                <li>2.51 – 3.50 = Baik</li>
                <li>1.51 – 2.50 = Cukup</li>
                <li>1.00 – 1.50 = Kurang</li>
            </ul>
        </div>

        <div>
            <h3 class="font-semibold text-lg text-blue-300 mb-2">6. Kerahasiaan Data</h3>
            <p>
                Semua data dan informasi yang diisi hanya digunakan untuk kepentingan evaluasi dan peningkatan mutu
                sekolah.
            </p>
        </div>
    </div>
</div>
